let current = 0;
const answers = new Array(questions.length).fill(null);

// Показываем текущий вопрос
function showQuestion() {
    document.getElementById("question").innerHTML = 
        `<strong>${current + 1}.</strong> ${questions[current]}`;
    
    document.getElementById("progress").innerText = 
        `${current + 1} из ${questions.length}`;
}

// Ответ пользователя
function answer(value) {
    answers[current] = value;
    
    if (current < questions.length - 1) {
        current++;
        showQuestion();
    } else {
        calculate();
    }
}

// Кнопка "Назад"
function prev() {
    if (current > 0) {
        current--;
        showQuestion();
    }
}

// Подсчёт результатов
function calculate() {
    const scales = {
        physical:    { yes: [1,25,33,48,55,62,68], no: [9,17,41] },
        indirect:    { yes: [2,18,34,42,56,63],   no: [10,26,49] },
        irritation:  { yes: [3,19,27,43,50,57,64,72], no: [11,35,69] },
        negativism:  { yes: [4,12,20,23,36] },
        resentment:  { yes: [5,13,21,29,37,51,58], no: [44] },
        suspicion:   { yes: [6,14,22,30,38,45,52,59], no: [65,70] },
        verbal:      { yes: [7,15,23,31,46,53,60,71,73], no: [39,74,75] },
        guilt:       { yes: [8,16,24,32,40,47,54,61,67] }
    };

    const count = (scale) => {
        let score = 0;
        if (scale.yes) scale.yes.forEach(i => { if (answers[i-1] === true) score++; });
        if (scale.no)  scale.no.forEach(i => { if (answers[i-1] === false) score++; });
        return score;
    };

    const result = {
        physical:    count(scales.physical),
        indirect:    count(scales.indirect),
        irritation:  count(scales.irritation),
        negativism:  count(scales.negativism),
        resentment:  count(scales.resentment),
        suspicion:   count(scales.suspicion),
        verbal:      count(scales.verbal),
        guilt:       count(scales.guilt)
    };

    const aggressionIndex = result.physical + result.irritation + result.verbal;
    const hostilityIndex  = result.resentment + result.suspicion;

    // Формируем красивый результат
    let html = `
        <h2>Твой результат по тесту Басса-Дарки</h2>
        
        <div class="result-box">
            <h3>Главные показатели</h3>
            <p><strong>Индекс агрессивности:</strong> <span class="big">${aggressionIndex}</span> 
                <span class="level ${aggressionIndex > 25 ? 'high' : aggressionIndex < 17 ? 'low' : 'normal'}">
                    (${aggressionIndex >= 17 && aggressionIndex <= 25 ? 'Норма' : aggressionIndex > 25 ? 'Повышен' : 'Понижен'})
                </span>
            </p>
            <p><strong>Индекс враждебности:</strong> <span class="big">${hostilityIndex}</span> 
                <span class="level ${hostilityIndex > 10 ? 'high' : hostilityIndex < 4 ? 'low' : 'normal'}">
                    (${hostilityIndex >= 4 && hostilityIndex <= 10 ? 'Норма' : hostilityIndex > 10 ? 'Повышен' : 'Понижен'})
                </span>
            </p>
        </div>

        <h3>Подробные шкалы</h3>
        <table class="results-table">
            <tr><th>Шкала</th><th>Баллы</th><th>Уровень</th></tr>
    `;

    const addRow = (name, score, max, low, high) => {
        let lvl = score > high ? "Высокий" : score < low ? "Низкий" : "Норма";
        let cls = score > high ? "high" : score < low ? "low" : "normal";
        html += `<tr><td>${name}</td><td><strong>${score}</strong> / ${max}</td><td class="${cls}">${lvl}</td></tr>`;
    };

    addRow("Физическая агрессия", result.physical, 10, 0, 4);
    addRow("Косвенная агрессия", result.indirect, 9, 0, 4);
    addRow("Раздражение", result.irritation, 11, 0, 5);
    addRow("Негативизм", result.negativism, 5, 0, 3);
    addRow("Обида", result.resentment, 8, 0, 4);
    addRow("Подозрительность", result.suspicion, 10, 0, 5);
    addRow("Вербальная агрессия", result.verbal, 13, 0, 6);
    addRow("Чувство вины", result.guilt, 9, 0, 5);

    html += `</table>`;

    html += `
        <div class="interpretation">
            <h3>Что это значит?</h3>
            <p><strong>Агрессивность — ${aggressionIndex} баллов:</strong> 
                ${aggressionIndex > 25 ? 'У тебя довольно высокий уровень агрессии. Ты легко можешь вспылить, сказать резкое слово или даже применить силу.' : 
                  aggressionIndex < 17 ? 'Агрессия у тебя низкая. Ты хорошо сдерживаешься и редко идёшь на конфликт.' : 
                  'Агрессия в пределах нормы — это хорошо.'}
            </p>
            <p><strong>Враждебность — ${hostilityIndex} баллов:</strong> 
                ${hostilityIndex > 10 ? 'Ты довольно недоверчив к людям, часто ожидаешь подвоха или плохого отношения.' : 
                  hostilityIndex < 4 ? 'Ты очень доброжелательный и доверчивый человек.' : 
                  'Враждебность в норме.'}
            </p>
            <small>Нормальные значения: агрессивность 17–25, враждебность 4–10</small>
        </div>

        <button onclick="location.reload()" class="restart-btn">Пройти тест ещё раз</button>
    `;

    document.getElementById("app").innerHTML = html;
}

